package de.alpharogroup.message.system.enums;

/**
 * The enumeration MessageState defines in which state a message is.
 */
public enum MessageState {

	/** The signed. */
	SIGNED,	
	/** The contacted. */
	CONTACTED,	
	/** The expelled. */
	EXPELLED,	
	/** The unreplied. */
	UNREPLIED,	
	/** The deleted. */
	DELETED;

}